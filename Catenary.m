function [l,h] = Catenary(H, V, w, s, E, A)

     l = H*s/(E*A) + ...
         H/w*( asinh(V/H) - asinh((V-w*s)/H) );

     h = -( (w*s)/(E*A) * ( V/w - s/2 ) + ...
         H/w * ( sqrt(1+(V/H)^2) - sqrt(1+( (V-w*s)/H )^2) ) );

     
